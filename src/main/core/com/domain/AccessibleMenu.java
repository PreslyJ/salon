package com.domain;

public class AccessibleMenu {
	private int accessibleMenuId;
	private int menuId;
	private int officerTypeId;
	private int nodeid;
	public int getAccessibleMenuId() {
		return accessibleMenuId;
	}
	public void setAccessibleMenuId(int accessibleMenuId) {
		this.accessibleMenuId = accessibleMenuId;
	}
	public int getMenuId() {
		return menuId;
	}
	public void setMenuId(int menuId) {
		this.menuId = menuId;
	}
	public int getOfficerTypeId() {
		return officerTypeId;
	}
	public void setOfficerTypeId(int officerTypeId) {
		this.officerTypeId = officerTypeId;
	}
	public int getNodeid() {
		return nodeid;
	}
	public void setNodeid(int nodeid) {
		this.nodeid = nodeid;
	}
	
	
}
